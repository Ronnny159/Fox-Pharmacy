SET SERVEROUTPUT ON;
SET ECHO ON;
SET LINESIZE 200;

ALTER SESSION SET NLS_DATE_FORMAT = 'DD/MM/YYYY';
ALTER SESSION SET NLS_NUMERIC_CHARACTERS = '.,';

-- Tabla USUARIO
CREATE TABLE USUARIO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_USUARIO VARCHAR2(50) NOT NULL UNIQUE,
    HASH_CONTRASENA VARCHAR2(128) NOT NULL,
    NOMBRE_COMPLETO VARCHAR2(200) NOT NULL,
    ROL NUMBER(1) NOT NULL CHECK (ROL IN (1, 2, 3, 4)),
    DOCUMENTO_IDENTIDAD VARCHAR2(20) NOT NULL UNIQUE,
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE
);

COMMENT ON TABLE USUARIO IS 'Usuarios del sistema: administradores, cajeros, farmaceuticos y auditores';
COMMENT ON COLUMN USUARIO.ROL IS '1=Administrador, 2=Cajero, 3=Farmaceutico, 4=Auditor';

-- Tabla PRODUCTO
CREATE TABLE PRODUCTO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODIGO VARCHAR2(50) NOT NULL UNIQUE,
    NOMBRE VARCHAR2(200) NOT NULL,
    DESCRIPCION VARCHAR2(500),
    ES_CONTROLADO NUMBER(1) DEFAULT 0 CHECK (ES_CONTROLADO IN (0, 1)),
    STOCK_MINIMO NUMBER(5) DEFAULT 0,
    DESCUENTO_PROXIMIDAD_VENCIMIENTO NUMBER(3,2),
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE
);

COMMENT ON TABLE PRODUCTO IS 'Catalogo maestro de medicamentos';
COMMENT ON COLUMN PRODUCTO.ES_CONTROLADO IS '0=No controlado, 1=Control especial';
COMMENT ON COLUMN PRODUCTO.DESCUENTO_PROXIMIDAD_VENCIMIENTO IS 'NULL=Usar descuento general del sistema';

-- Tabla CLIENTE
CREATE TABLE CLIENTE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DOCUMENTO VARCHAR2(20) NOT NULL UNIQUE,
    NOMBRE_COMPLETO VARCHAR2(200) NOT NULL,
    TELEFONO VARCHAR2(20) NOT NULL,
    CORREO VARCHAR2(100),
    CHAT_ID VARCHAR2(100),
    MEDICAMENTO_RECURRENTE VARCHAR2(200),
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE
);

COMMENT ON TABLE CLIENTE IS 'Pacientes del programa de fidelizacion';
COMMENT ON COLUMN CLIENTE.CHAT_ID IS 'ID de chat de Telegram para notificaciones';

-- Tabla LOTE
CREATE TABLE LOTE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODIGO_LOTE VARCHAR2(100) NOT NULL,
    PRODUCTO_ID NUMBER NOT NULL,
    FECHA_FABRICACION DATE NOT NULL,
    FECHA_VENCIMIENTO DATE NOT NULL,
    PRECIO_COMPRA NUMBER(10,2) NOT NULL,
    PRECIO_VENTA NUMBER(10,2) NOT NULL,
    CANTIDAD_ACTUAL NUMBER(6) NOT NULL,
    CANTIDAD_INICIAL NUMBER(6) NOT NULL,
    ESTADO NUMBER(1) DEFAULT 1 CHECK (ESTADO IN (1, 2, 3, 4)),
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_LOTE_PRODUCTO FOREIGN KEY (PRODUCTO_ID) REFERENCES PRODUCTO(ID)
);

COMMENT ON TABLE LOTE IS 'Control granular de inventario. Base para FEFO';
COMMENT ON COLUMN LOTE.ESTADO IS '1=Activo, 2=Agotado, 3=Vencido, 4=En cuarentena';

-- Tabla VENTA
CREATE TABLE VENTA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NUMERO_FACTURA VARCHAR2(20) NOT NULL UNIQUE,
    FECHA_VENTA DATE DEFAULT SYSDATE NOT NULL,
    USUARIO_ID NUMBER NOT NULL,
    CLIENTE_ID NUMBER,
    SUBTOTAL NUMBER(12,2) NOT NULL,
    DESCUENTO_TOTAL NUMBER(12,2) DEFAULT 0,
    TOTAL NUMBER(12,2) NOT NULL,
    ANULADA NUMBER(1) DEFAULT 0 CHECK (ANULADA IN (0, 1)),
    FECHA_ANULACION DATE,
    ANULADA_POR NUMBER,
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_VENTA_USUARIO FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO(ID),
    CONSTRAINT FK_VENTA_CLIENTE FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTE(ID)
);

COMMENT ON TABLE VENTA IS 'Cabecera de facturas de venta';

-- Tabla DETALLE_VENTA
CREATE TABLE DETALLE_VENTA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VENTA_ID NUMBER NOT NULL,
    LOTE_ID NUMBER NOT NULL,
    CANTIDAD NUMBER(6) NOT NULL,
    PRECIO_APLICADO NUMBER(10,2) NOT NULL,
    DESCUENTO_UNITARIO NUMBER(10,2) DEFAULT 0,
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_DETALLE_VENTA FOREIGN KEY (VENTA_ID) REFERENCES VENTA(ID),
    CONSTRAINT FK_DETALLE_LOTE FOREIGN KEY (LOTE_ID) REFERENCES LOTE(ID)
);

COMMENT ON TABLE DETALLE_VENTA IS 'Trazabilidad por item vendido con lote exacto';

-- Tabla AJUSTE_INVENTARIO
CREATE TABLE AJUSTE_INVENTARIO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LOTE_ID NUMBER NOT NULL,
    TIPO VARCHAR2(30) NOT NULL CHECK (TIPO IN ('Averia', 'Vencido', 'RetiroLegal', 'ConteoCiclico')),
    CANTIDAD NUMBER(6) NOT NULL,
    MOTIVO VARCHAR2(500) NOT NULL,
    FECHA_AJUSTE DATE DEFAULT SYSDATE NOT NULL,
    RESPONSABLE_ID NUMBER NOT NULL,
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_AJUSTE_LOTE FOREIGN KEY (LOTE_ID) REFERENCES LOTE(ID),
    CONSTRAINT FK_AJUSTE_USUARIO FOREIGN KEY (RESPONSABLE_ID) REFERENCES USUARIO(ID)
);

COMMENT ON TABLE AJUSTE_INVENTARIO IS 'Auditoria de mermas, averias y retiros';

-- Tabla PARAMETRO_SISTEMA
CREATE TABLE PARAMETRO_SISTEMA (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLAVE VARCHAR2(100) NOT NULL UNIQUE,
    VALOR VARCHAR2(50) NOT NULL,
    DESCRIPCION VARCHAR2(500),
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE
);

COMMENT ON TABLE PARAMETRO_SISTEMA IS 'Configuracion global del sistema';

-- Tabla HISTORIAL_PARAMETRO
CREATE TABLE HISTORIAL_PARAMETRO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLAVE_PARAMETRO VARCHAR2(100) NOT NULL,
    VALOR_ANTERIOR VARCHAR2(50) NOT NULL,
    VALOR_NUEVO VARCHAR2(50) NOT NULL,
    MOTIVO VARCHAR2(500) NOT NULL,
    FECHA_CAMBIO DATE DEFAULT SYSDATE NOT NULL,
    MODIFICADO_POR_ID NUMBER NOT NULL,
    DIRECCION_IP VARCHAR2(50),
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_HP_USUARIO FOREIGN KEY (MODIFICADO_POR_ID) REFERENCES USUARIO(ID)
);

COMMENT ON TABLE HISTORIAL_PARAMETRO IS 'Auditoria de cambios en parametros';

-- Tabla HISTORIAL_DESCUENTO_PRODUCTO
CREATE TABLE HISTORIAL_DESCUENTO_PRODUCTO (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCTO_ID NUMBER NOT NULL,
    CODIGO_PRODUCTO VARCHAR2(50) NOT NULL,
    NOMBRE_PRODUCTO VARCHAR2(200) NOT NULL,
    DESCUENTO_ANTERIOR NUMBER(3,2),
    DESCUENTO_NUEVO NUMBER(3,2),
    ACCION VARCHAR2(20) NOT NULL CHECK (ACCION IN ('CREAR', 'MODIFICAR', 'ELIMINAR')),
    MOTIVO VARCHAR2(500) NOT NULL,
    FECHA_CAMBIO DATE DEFAULT SYSDATE NOT NULL,
    MODIFICADO_POR_ID NUMBER NOT NULL,
    DIRECCION_IP VARCHAR2(50),
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_HDP_PRODUCTO FOREIGN KEY (PRODUCTO_ID) REFERENCES PRODUCTO(ID),
    CONSTRAINT FK_HDP_USUARIO FOREIGN KEY (MODIFICADO_POR_ID) REFERENCES USUARIO(ID)
);

COMMENT ON TABLE HISTORIAL_DESCUENTO_PRODUCTO IS 'Auditoria de descuentos individuales';

-- Tabla ALERTA_INFLACION
CREATE TABLE ALERTA_INFLACION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCTO_ID NUMBER NOT NULL,
    CODIGO_PRODUCTO VARCHAR2(50) NOT NULL,
    PRECIO_ANTERIOR NUMBER(10,2) NOT NULL,
    PRECIO_NUEVO NUMBER(10,2) NOT NULL,
    PORCENTAJE_INCREMENTO NUMBER(5,2) NOT NULL,
    FECHA_ALERTA DATE DEFAULT SYSDATE NOT NULL,
    ATENDIDA NUMBER(1) DEFAULT 0 CHECK (ATENDIDA IN (0, 1)),
    ATENDIDA_POR NUMBER,
    FECHA_ATENCION DATE,
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_AI_PRODUCTO FOREIGN KEY (PRODUCTO_ID) REFERENCES PRODUCTO(ID),
    CONSTRAINT FK_AI_USUARIO FOREIGN KEY (ATENDIDA_POR) REFERENCES USUARIO(ID)
);

COMMENT ON TABLE ALERTA_INFLACION IS 'Alertas por incremento de costos';

-- Tabla ALERTA_FIDELIZACION
CREATE TABLE ALERTA_FIDELIZACION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CLIENTE_ID NUMBER NOT NULL,
    PRODUCTO_ID NUMBER,
    LOTE_ID NUMBER,
    TIPO_ALERTA VARCHAR2(30) NOT NULL,
    MENSAJE VARCHAR2(500) NOT NULL,
    FECHA_ENVIO DATE DEFAULT SYSDATE,
    ENVIADA NUMBER(1) DEFAULT 1,
    LEIDA NUMBER(1) DEFAULT 0,
    FECHA_LEIDA DATE,
    ACTIVO NUMBER(1) DEFAULT 1,
    FECHA_CREACION DATE DEFAULT SYSDATE,
    CONSTRAINT FK_AF_CLIENTE FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTE(ID),
    CONSTRAINT FK_AF_PRODUCTO FOREIGN KEY (PRODUCTO_ID) REFERENCES PRODUCTO(ID),
    CONSTRAINT FK_AF_LOTE FOREIGN KEY (LOTE_ID) REFERENCES LOTE(ID)
);

COMMENT ON TABLE ALERTA_FIDELIZACION IS 'Notificaciones del programa de fidelizacion';

COMMIT;

